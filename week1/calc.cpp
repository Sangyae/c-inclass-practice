#include <iostream>

using namespace std;



int main() {

   int a, b;


   cout << "Enter the first integer: "; 

   cin >> a;

   cout << "Enter the second integer: ";

   cin >> b;

   cout << "Addition (a + b): " << (a + b) << endl;

   cout << "Subtraction (a - b): " << (a - b) << endl;

   cout << "Multiplication (a * b): " << (a * b) << endl;

   cout << "Division (a / b): " << (a / b) << endl;

   cout << "Modulo (a % b): " << (a % b) << endl;

 

   return 0;

}

